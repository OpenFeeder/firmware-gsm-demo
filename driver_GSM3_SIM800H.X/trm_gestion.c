/*------------------------------> V A R I A B L E - L O C A L <----------------*/
volatile int16_t delay = 0;

/*------------------------------> P U B L I C - F U N C T I O N S <------------*/

/*********************************************************************
 * Function:        void tmr_delay(int delay_ms)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        None
 *
 * Note:            None
 ********************************************************************/
void tmr_delay(int16_t delay_ms) {
    delay = delay_ms;
    while (delay > 0) {
    }
}

/*********************************************************************
 * Function:        void tmr_callBack(void)
 *
 * PreCondition:    None
 *
 * Input:           None
 *
 * Output:          None
 *
 * Side Effects:    None
 *
 * Overview:        None
 *
 * Note:            None
 ********************************************************************/
void tmr_callBack() {
    if (delay > 0) --delay;
}


